print("=== โปรแกรมคำนวณพื้นที่และค่าปูพื้นห้อง ===")
    
    # 1. รับค่าข้อมูลจากผู้ใช้ (ตัวแปรภาษาอังกฤษ ข้อความภาษาไทย)
width = float(input("ใส่ความกว้างของห้อง (เมตร): "))
length = float(input("ใส่ความยาวของห้อง (เมตร): "))
price_per_sqm = float(input("ใส่ราคากระเบื้องต่อตารางเมตร (บาท): "))
    
    # 2. คำนวณค่าพื้นที่และราคารวม
area = width * length
total_cost = area * price_per_sqm
    
    # 3. แสดงผลลัพธ์
print("-" * 40)
print(f"พื้นที่ทั้งหมด: {area:.2f} ตารางเมตร")
print(f"งบประมาณค่าใช้จ่ายทั้งหมด: {total_cost:,.2f} บาท")
print("-" * 40)

print("ข้อผิดพลาด: กรุณากรอกเฉพาะตัวเลขที่มากกว่า 0 เท่านั้น!")